public class Author {
    private String name;
    private int quantity_of_publishing;

    public Author() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQuantity_of_publishing() {
        return quantity_of_publishing;
    }

    public void setQuantity_of_publishing(int quantity_of_publishing) {
        this.quantity_of_publishing = quantity_of_publishing;
    }
}
